/**
 * dup_chars - Func to duplicate Char
 * @pathstr: PATH Str
 * @start: start index
 * @stop: stop index
 *
 * Return: Pointer to the new buffer
 */
char *dup_chars(char *pathstr, int start, int stop)
{
	static char buf[1024];
	int i = 0, k = 0;

	for (k = 0, i = start; i < stop; i++)
		if (pathstr[i] != ':')
			buf[k++] = pathstr[i];
	buf[k] = 0;
	return (buf);
}

