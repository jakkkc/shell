/**
 * is_cmd - Func to determine if file is an executable CMD
 * @info: Information struct
 * @path: Path to File
 *
 * Return: 1 if true/ successful, else 0
 */
int is_cmd(info_t *info, char *path)
{
	struct stat st;

	(void)info;
	if (!path || stat(path, &st))
		return (0);

	if (st.st_mode & S_IFREG)
	{
		return (1);
	}
	return (0);
}

